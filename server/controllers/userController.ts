// // Controller function to toggle project publish
export const togglePublish = async (req: Request, res: Response) => {
  try {
    const userId = req.userId;
    if (!userId) {
      return res.status(401).json({ message: "Unauthorized" });
    }

    const { projectId } = req.params;
    const project = await prisma.websiteProject.findUnique({
      where: { id: projectId, userId },
    });

    if (!project) {
      return res.status(404).json({ message: "Project not found" });
    }
    await prisma.websiteProject.update({
      where: { id: projectId, userId },
      data: { isPublished: !project.isPublished },
    });

    res.json({
      message: project.isPublished
        ? "Projected Unpublished"
        : "Project Published",
    });

    // catch
  } catch (error: any) {
    console.log(error.code || error.message);
    res.status(500).json({ messsage: error.message });
  }
};

// // Controller function to purchase credits
export const purchaseCredits = async (req: Request, res: Response) => {
  try {
    // ✅ Define available plans
    interface Plan {
      credits: number;
      amount: number; // USD amount
    }

    const plans: Record<string, Plan> = {
      basic: { credits: 100, amount: 5 },
      pro: { credits: 400, amount: 19 },
      enterprise: { credits: 1000, amount: 49 },
    };

    // ✅ Extract user and plan info
    const userId = req.userId;
    const { planId } = req.body as { planId: string };
    const origin = req.headers.origin || "http://localhost:5173";

    if (!userId) {
      return res.status(401).json({ message: "User not authenticated" });
    }

    const plan = plans[planId];
    if (!plan) {
      return res.status(404).json({ message: "Plan not found" });
    }

    // ✅ Create transaction in DB
    const transaction = await prisma.transaction.create({
      data: {
        userId: userId,
        planId: planId, // Must match Prisma schema type (String!)
        amount: plan.amount, // number
        credits: plan.credits, // number
        isPaid: false,
      },
    });

    // ✅ Create Stripe checkout session
    const stripe = new Stripe(process.env.STRIPE_SECRET_KEY as string);

    const session = await stripe.checkout.sessions.create({
      success_url: `${origin}/loading`,
      cancel_url: `${origin}`,
      payment_method_types: ["card"],
      line_items: [
        {
          price_data: {
            currency: "usd",
            product_data: {
              name: `AiSiteBuilder - ${plan.credits} credits`,
            },
            // Stripe expects unit_amount in cents, must be integer
            unit_amount: Math.round(plan.amount * 100),
          },
          quantity: 1,
        },
      ],
      mode: "payment",
      metadata: {
        transactionId: transaction.id,
        appId: "ai-site-builder",
      },
      expires_at: Math.floor(Date.now() / 1000) + 30 * 60, // Expires in 30 mins
    });

    res.json({ payment_link: session.url });
  } catch (error: any) {
    console.error("PURCHASE CREDITS ERROR:", error);
    res.status(500).json({ message: error.message || "Internal Server Error" });
  }
};
